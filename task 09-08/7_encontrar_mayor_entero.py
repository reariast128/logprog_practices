'''
Ejercicio 7

Escriba un programa que permita hallar el mayor entero contenido de un número decimal cualquiera (sea positivo o negativo).
'''

print("Este programa muestra el mayor entero contenido en un decimal cualquiera.")
numero = input("Introduce aquí tu número -> ")
mayor_numero = max(numero)

print(f"\nEl mayor entero de {numero} es {mayor_numero}")